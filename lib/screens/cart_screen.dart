// The cart data changes (the list of items, the total amount, the item count, etc.).

// The icon button itself does not change. It’s always a shopping cart icon with the same color and same onPressed callback.

// So, rebuilding this button every time the cart updates would be wasted work — the widget looks exactly the same.
